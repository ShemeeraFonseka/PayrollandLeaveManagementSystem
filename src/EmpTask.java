    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String empID=txtid.getText();
String month=txtmonth.getSelectedItem().toString();
        try
{
        JasperDesign design = JRXmlLoader.load("D:\\DCSD\\EAD\\Payroll and Leave Management System\\src\\report4.jrxml"); 
        JRDesignQuery query =  new JRDesignQuery();
    query.setText("select * from empsal where empid='"+empID+"'and month='"+month+"'");
    design.setQuery(query);
    JasperReport report = JasperCompileManager.compileReport(design);
    JasperPrint print = JasperFillManager.fillReport(report,null,mydbconnection.getConnection());
    JasperViewer.viewReport(print);}
catch(Exception e)
{
    JOptionPane.showMessageDialog(null, e.getMessage());
    
}        
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
